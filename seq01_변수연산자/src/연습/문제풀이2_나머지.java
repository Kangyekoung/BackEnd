package 연습;

public class 문제풀이2_나머지 {
	public static void main(String[] args) {
		int num1 = 30;
		int num2 = 40;
		
		int sum = num1 + num2;
		System.out.println("합계 : " + sum);
		double div = (double)num1 / num2;
		System.out.println("나눈값 : " + div);
		int mok = num1 % num2;
		System.out.println("합계 : " + mok);
		
	}
}
